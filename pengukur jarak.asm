
NAME "DESTYAWAN"
INCLUDE 'emu8086.inc'
ORG 100H
JMP MULAI
PSN1    DB "||   MENGHITUNG JARAK  ||",13,10
        DB "=========================",13,10,13,10,'$'
V0      DB "MASUKKAN KECEPATAN      : $"
t       DB "MASUKAN WAKTU           : $"
S       DB "JARAK                   : $"
PJG     DW ?
TOT     DW ?
ARE     DW ?


MULAI:
LEA DX,PSN1
MOV AH,9
INT 21H

LEA DX,V0
MOV AH,9
INT 21H

CALL SCAN_NUM

MOV PJG,CX;KECEPATAN

PUTC 13
PUTC 10

LEA DX,t
MOV AH,9
INT 21H

CALL SCAN_NUM  ;WAKTUUU

MOV TOT,CX 

MOV AX,TOT
MOV BX,PJG ;V0 . T
MUL BX
PUSH AX

PUTC 13
PUTC 10

LEA DX,S
MOV AH,9
INT 21H

POP AX
MOV ARE,AX

CALL PRINT_NUM

PUTC 13
PUTC 10

MOV AH,0
INT 16H

RET

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
END
